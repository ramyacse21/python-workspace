# String
text = "python"
print("'p' in string:", 'p' in text)         # True
print("'z' not in string:", 'z' not in text) # True

# List
my_list = [10, 20, 30]
print("20 in list:", 20 in my_list)          # True
print("50 not in list:", 50 not in my_list)  # True

# Tuple
my_tuple = (1, 2, 3)
print("2 in tuple:", 2 in my_tuple)          # True

# Set
my_set = {100, 200, 300}
print("100 in set:", 100 in my_set)          # True

# Dictionary (checks keys)
my_dict = {"name": "Ramya", "age": 21}
print("'name' in dict:", 'name' in my_dict)  # True
print("'Ramya' in dict:", 'Ramya' in my_dict)  # False (not a key)



